//详细功能说明：
// - 提供 users / nodes / applications / permissions / session_logs 五张表的增删改查
// - 本模块不依赖 Qt，仅使用标准库与 sqlite3 C API
// - 错误码约定：0 成功，小于 0 为失败（见 manage_db_err）
//
////////////////////////////////////////////////////////////////////////////////////////////////////////

#ifndef MANAGE_DB_H
#define MANAGE_DB_H

#include <cstdint>
#include <string>
#include <vector>

enum manage_db_err {
	manage_db_ok = 0,
	manage_db_err_open = -1,
	manage_db_err_sql = -2,
	manage_db_err_not_found = -3,
	manage_db_err_constraint = -4,
	manage_db_err_invalid_arg = -5,
};

struct sqlite3;

struct user_row {
	std::int64_t user_id = 0;
	std::string username;
	std::string password_hash;
	std::string role;
	std::string created_at;
};

struct node_row {
	std::int64_t node_id = 0;
	std::string node_name;
	std::string auth_key;
	int is_online = 0;
	std::string last_seen;
	std::string created_at;
};

struct application_row {
	std::int64_t app_id = 0;
	std::int64_t node_id = 0;
	std::string display_name;
	std::string exe_path;
	std::string work_dir;
	std::string icon_data;
	int is_public = 0;
};

struct permission_row {
	std::int64_t perm_id = 0;
	std::int64_t user_id = 0;
	std::int64_t app_id = 0;
};

struct session_log_row {
	std::string session_id;
	std::int64_t user_id = 0;
	std::int64_t app_id = 0;
	std::int64_t node_id = 0;
	std::string start_time;
	std::string end_time;
	std::int64_t duration = 0;
	std::string exit_reason;
};

class manage_db {
public:
	manage_db();
	~manage_db();

	manage_db(const manage_db &) = delete;
	manage_db &operator=(const manage_db &) = delete;

	/////////////////////////////////////////////////////////////////////////////
	/// @说明
	///          打开 SQLite 数据库文件
	/// @参数
	///          db_path--数据库路径（UTF-8）
	/// @返回值 正常值。0:成功，小于0对应错误码
	///
	/// @时间    2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int open(const char *db_path);

	/////////////////////////////////////////////////////////////////////////////
	/// @说明
	///          关闭数据库连接
	/// @参数
	///          无
	/// @返回值 无
	///
	/// @时间    2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	void close();

	/////////////////////////////////////////////////////////////////////////////
	/// @说明
	///          是否已打开数据库
	/// @参数
	///          无
	/// @返回值 true 已打开
	///
	/// @时间    2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	bool is_open() const;
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          最近一次 user_insert 在 sqlite3_step 失败时快照的错误信息
	///                （须在 step 后立刻保存；后续 SELECT 会清空连接上的 sqlite3_errmsg）
	/////////////////////////////////////////////////////////////////////////////
	std::string sqlite_last_errmsg() const;
	int sqlite_extended_errcode() const;
	int sqlite_last_step_return_code() const;

	// ---------- users ----------
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          新增用户记录（created_at 为空则使用数据库默认当前时间）
	/// @参数          row--字段；new_user_id--输出新 user_id
	/// @返回值        0 成功，小于 0 错误码
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int user_insert(const user_row &row, std::int64_t *new_user_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 user_id 更新用户（须设置 row.user_id）
	/// @参数          row--完整记录
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int user_update(const user_row &row);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 user_id 删除用户
	/// @参数          user_id--主键
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int user_delete(std::int64_t user_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 user_id 查询单条
	/// @参数          user_id--主键；out--输出
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int user_select_by_id(std::int64_t user_id, user_row *out);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 username（唯一）查询单条
	/// @参数          username--登录名；out--输出
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int user_select_by_username(const std::string &username, user_row *out);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          查询全部用户（按 user_id 排序）
	/// @参数          out--输出列表
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int user_select_all(std::vector<user_row> *out);

	// ---------- nodes ----------
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          新增节点（时间字段为空时使用默认）
	/// @参数          row--字段；new_node_id--输出新 node_id
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int node_insert(const node_row &row, std::int64_t *new_node_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 node_id 更新节点
	/// @参数          row--须设置 row.node_id
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int node_update(const node_row &row);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 node_id 删除节点（将级联删除关联 applications）
	/// @参数          node_id--主键
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int node_delete(std::int64_t node_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 node_id 查询单条
	/// @参数          node_id--主键；out--输出
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int node_select_by_id(std::int64_t node_id, node_row *out);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 node_name（唯一）查询单条
	/// @参数          node_name--节点名；out--输出
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int node_select_by_name(const std::string &node_name, node_row *out);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          查询全部节点
	/// @参数          out--输出列表
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int node_select_all(std::vector<node_row> *out);

	// ---------- applications ----------
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          新增应用条目（归属某 node）
	/// @参数          row--字段；new_app_id--输出新 app_id
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int application_insert(const application_row &row, std::int64_t *new_app_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 app_id 更新应用信息
	/// @参数          row--须设置 row.app_id、row.node_id
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int application_update(const application_row &row);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 app_id 删除应用
	/// @参数          app_id--主键
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int application_delete(std::int64_t app_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 app_id 查询单条
	/// @参数          app_id--主键；out--输出
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int application_select_by_id(std::int64_t app_id, application_row *out);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 node_id 查询该节点下全部应用
	/// @参数          node_id--外键；out--输出列表
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int application_select_by_node(std::int64_t node_id, std::vector<application_row> *out);

	// ---------- permissions ----------
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          新增用户对应用的访问权限（user_id+app_id 唯一）
	/// @参数          row--字段；new_perm_id--输出新 perm_id
	/// @返回值        0 成功；约束冲突返回 manage_db_err_constraint
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int permission_insert(const permission_row &row, std::int64_t *new_perm_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 perm_id 删除权限
	/// @参数          perm_id--主键
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int permission_delete_by_id(std::int64_t perm_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 user_id 与 app_id 删除权限
	/// @参数          user_id、app_id--联合定位
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int permission_delete_by_pair(std::int64_t user_id, std::int64_t app_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 perm_id 查询单条
	/// @参数          perm_id--主键；out--输出
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int permission_select_by_id(std::int64_t perm_id, permission_row *out);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          查询全部权限记录
	/// @参数          out--输出列表
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int permission_select_all(std::vector<permission_row> *out);

	// ---------- session_logs ----------
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          插入会话日志（session_id 建议使用 UUID 文本）
	/// @参数          row--须含 session_id、user_id、app_id、node_id、start_time
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int session_log_insert(const session_log_row &row);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          更新会话结束信息（结束时间、时长、退出原因）
	/// @参数          session_id--主键；其余为结束态字段
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int session_log_update_end(const std::string &session_id, const std::string &end_time,
	                           std::int64_t duration, const std::string &exit_reason);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 session_id 删除日志
	/// @参数          session_id--主键
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int session_log_delete(const std::string &session_id);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          按 session_id 查询单条会话日志
	/// @参数          session_id--主键；out--输出
	/// @返回值        0 成功；manage_db_err_not_found 无此行
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int session_log_select(const std::string &session_id, session_log_row *out);
	/////////////////////////////////////////////////////////////////////////////
	/// @说明          查询全部会话日志（按 start_time 排序）
	/// @参数          out--输出列表
	/// @返回值        0 成功
	/// @时间          2026/5/7
	/////////////////////////////////////////////////////////////////////////////
	int session_log_select_all(std::vector<session_log_row> *out);

private:
	sqlite3 *m_db;
	std::string m_last_stmt_errmsg;
	int m_last_stmt_extended_errcode = 0;
	int m_last_stmt_step_rc = 0;
};

#endif
